 # Medea
 
 - - -
 
 Medea is (yet another) JSON library for Swift.  Its key features are:
 
 - Written in pure Swift.  It will work anywhere Swift works.
 - Uses Swift's `indirect` `enum` to wrap JSON values.  
 - Ensures rings type-safety via explicit and exhaustive checks using Swift's `switch` statement.
 - Implements native Swift error handling mechanism using `do-catch` statement.
 - Can convert to/from Foundation's representation of JSON.

